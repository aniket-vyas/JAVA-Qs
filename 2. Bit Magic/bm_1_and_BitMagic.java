//  A N D ( & )

public class bm_1_and_BitMagic {
	public static void main(String[] args) {
		int x = 3 , y = 6;
		System.out.println(x & y);
	}
}

// x = 3    0....0011   (in binary)
// y = 6    0....0110
//---------------------
// x & y    0....0010   (in decimal : 2)
//---------------------


// OUTPUT :- 2
