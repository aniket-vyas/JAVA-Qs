// OR Bit Magic

public class bm_2_or_{
  public static void main(String[] args) {
		int x = 3 , y = 6;
		System.out.println(x | y);
	}
}

// x     : 0....0011  (3 in binary)
// y     : 0....0110  (6 in binary)
//--------------------
// x | y : 0....0111  (7 in decimal)
//--------------------

// OUTPUT : 7
